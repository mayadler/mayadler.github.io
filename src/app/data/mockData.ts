export function getRiskColor(risk: number): string {
  if (risk <= 25) return '#1A56DB'; // blue - low
  if (risk <= 45) return '#0D9488'; // teal - moderate
  if (risk <= 65) return '#EA580C'; // orange - elevated
  return '#DC2626'; // red - high
}

export function getRiskLabel(risk: number): string {
  if (risk <= 25) return 'Low';
  if (risk <= 45) return 'Moderate';
  if (risk <= 65) return 'Elevated';
  return 'High';
}

